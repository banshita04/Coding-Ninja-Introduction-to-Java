package fundamentals;
public class ContinueKeyword {
    public static void main(String args[]) {
      int n=5;
      for(int i=1;i<=n;){
          
              if(i==4){
              
              continue;
              }
              i++;
              System.out.println(i);
          
      }
      System.out.println();
    }
}
      